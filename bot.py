import requests
import os
import yfinance as yf
from datetime import datetime
import pytz

# --- бҹЎ. бһҖбһ¶бһҡбһҖбҹҶбһҺбһҸбҹӢ (бһҷбһҖбһ…бҹҒбһүбһ–бһё GitHub Secrets) ---
TOKEN = os.getenv('TELEGRAM_TOKEN')
GROUP_ID = os.getenv('TELEGRAM_ID')
TOPIC_ANALYSIS = os.getenv('TOPIC_ANALYSIS')
TOPIC_ALERTS = os.getenv('TOPIC_ALERTS')

def get_smc_data():
    try:
        # бһ‘бһ¶бһүбһҷбһҖбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһҳбһ¶бһҹ (GC=F) бһҡбһҷбҹҲбһ–бҹҒбһӣ бҹҘ бһҗбҹ’бһ„бҹғ бһҖбһҳбҹ’бһҡбһ·бһҸ бҹЎ бһҳбҹүбҹ„бһ„ (1h)
        gold = yf.Ticker("GC=F")
        df = gold.history(period="5d", interval="1h")
        
        if df.empty or len(df) < 24:
            return None

        # --- бҹў. бһӮбһҺбһ“бһ¶ Liquidity Levels (SMC Logic) ---
        # PDH & PDL: бһҡбһҖбһҸбһҳбҹ’бһӣбҹғбһҒбҹ’бһ–бһҹбҹӢ/бһ‘бһ¶бһ”бһ”бҹҶбһ•бһ»бһҸбһҖбҹ’бһ“бһ»бһ„бһ…бҹҶбһҺбҹ„бһҳ бҹўбҹӨ бһ‘бҹҖбһ“бһҳбһ»бһ“ (бһҳбҹ’бһҹбһ·бһӣбһҳбһ·бһү)
        pdh = df['High'].iloc[-25:-1].max()
        pdl = df['Low'].iloc[-25:-1].min()

        # Asia Session Range (бһ…бһ“бҹ’бһӣбҹ„бҹҮбһҳбҹүбҹ„бһ„ 00:00 - 08:00 UTC)
        # бһ…бҹҶбһҺбһ¶бҹҶбҹ– бһҳбҹүбҹ„бһ„бһ“бҹҒбҹҮбһўбһ¶бһ…бһ”бҹ’бһҡбҹӮбһ”бҹ’бһҡбһҪбһӣбһ”бһ“бҹ’бһҸбһ·бһ…бһ”бһ“бҹ’бһҸбһҪбһ…бһҸбһ¶бһҳбһҡбһҠбһјбһңбһҖбһ¶бһӣ бһ”бҹүбһ»бһ“бҹ’бһҸбҹӮбһҮбһ¶бһ‘бһјбһ‘бҹ…бһӮбһәбһҳбҹүбҹ„бһ„бһ“бҹҒбҹҮ
        asia_data = df.between_time('00:00', '08:00')
        asia_high = asia_data['High'].max() if not asia_data.empty else pdh
        asia_low = asia_data['Low'].min() if not asia_data.empty else pdl

        # Order Block (OB): бһҡбһҖ Swing бһ…бһ»бһ„бһҖбҹ’бһҡбҹ„бһҷбһҖбҹ’бһ“бһ»бһ„бһҡбһҷбҹҲбһ–бҹҒбһӣ бҹў бһҗбҹ’бһ„бҹғ (бҹӨбҹЁ бһҳбҹүбҹ„бһ„)
        supply_ob = df['High'].iloc[-48:-1].max()
        demand_ob = df['Low'].iloc[-48:-1].min()

        current_price = df['Close'].iloc[-1]
        prev_close = df['Close'].iloc[-2]
        change_pct = ((current_price - prev_close) / prev_close) * 100

        return {
            "price": current_price,
            "pdh": pdh,
            "pdl": pdl,
            "asia_high": asia_high,
            "asia_low": asia_low,
            "supply_ob": supply_ob,
            "demand_ob": demand_ob,
            "pct": change_pct
        }
    except Exception as e:
        print(f"вқҢ Error fetching data: {e}")
        return None

def send_msg(text, topic_id):
    if not topic_id or not TOKEN or not GROUP_ID:
        print("вқҢ Missing Telegram configurations")
        return
    url = f"https://api.telegram.org/bot{TOKEN}/sendMessage"
    payload = {
        "chat_id": GROUP_ID,
        "text": text,
        "parse_mode": "Markdown",
        "message_thread_id": topic_id
    }
    try:
        r = requests.post(url, data=payload)
        print(f"рҹ“Ў Sent to Topic {topic_id}: {r.json().get('ok')}")
    except Exception as e:
        print(f"вқҢ Error sending message: {e}")

def main():
    data = get_smc_data()
    if not data:
        print("вқҢ Could not process SMC data")
        return
    
    kh_tz = pytz.timezone("Asia/Phnom_Penh")
    now_kh = datetime.now(kh_tz)
    price = data['price']

    # --- бҹЈ. бһҖбһ¶бһҡбһ•бҹ’бһүбһҫ REPORT бһҸбһ¶бһҳ SESSION (бһҳбҹүбҹ„бһ„ бҹЁ, бҹЎбҹӨ, бҹЎбҹ©) ---
    if now_kh.hour in [8, 14, 19] and now_kh.minute < 15:
        session = "рҹҢҸ ASIA" if now_kh.hour == 8 else "рҹҮӘрҹҮә LONDON" if now_kh.hour == 14 else "рҹҮәрҹҮё NEW YORK"
        
        report = (
            f"рҹҺҜ **{session} SESSION | SMC MAP**\n"
            f"рҹ“… `{now_kh.strftime('%d/%m/%Y | %H:%M')}`\n"
            f"в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n\n"
            f"рҹ’° **Current Price:** `${price:,.2f}`\n\n"
            f"рҹӣ‘ **LIQUIDITY LEVELS (Targets)**\n"
            f"вҖў PDH (Prev. Day High): `${data['pdh']:,.2f}`\n"
            f"вҖў PDL (Prev. Day Low): `${data['pdl']:,.2f}`\n"
            f"вҖў Asia High (BSL): `${data['asia_high']:,.2f}`\n"
            f"вҖў Asia Low (SSL): `${data['asia_low']:,.2f}`\n\n"
            f"рҹ§ұ **INSTITUTIONAL ZONES (OB)**\n"
            f"вҖў Supply OB (Sell Zone): `${data['supply_ob']:,.2f}`\n"
            f"вҖў Demand OB (Buy Zone): `${data['demand_ob']:,.2f}`\n\n"
            f"вҡЎ **Trend Bias:** {'рҹҹў Bullish' if data['pct'] > 0 else 'рҹ”ҙ Bearish'}\n"
            f"в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n"
            f"Generated by **Titanium321 AI** рҹҡҖ"
        )
        send_msg(report, TOPIC_ANALYSIS)

    # --- бҹӨ. бһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’ ALERT LIQUIDITY SWEEP (бһҡбһҸбҹӢбһҡбһ¶бһӣбҹӢ бҹЎбҹҘ бһ“бһ¶бһ‘бһё) ---
    # бһ”бҹ’бһҡбһ¶бһ”бҹӢбһ–бҹҒбһӣбһҸбһҳбҹ’бһӣбҹғбһ”бҹҶбһ”бҹӮбһҖ PDH (Buyside Liquidity Taken)
    if price > data['pdh']:
        alert = f"рҹҡЁ **SMC ALERT: PDH SWEEP!**\n\nбһҸбһҳбҹ’бһӣбҹғбһ”бһ¶бһ“бһ”бҹҶбһ”бҹӮбһҖ High бһҳбҹ’бһҹбһ·бһӣбһҳбһ·бһү: `${price:,.2f}`\nвҡ пёҸ бһўбһ¶бһ…бһҮбһ¶бһҖбһ¶бһҡбһҷбһҖ Liquidity (BSL)бҹ” бһҡбһ„бҹӢбһ…бһ¶бҹҶбһҳбһҫбһӣбһҹбһүбҹ’бһүбһ¶ Reversal!"
        send_msg(alert, TOPIC_ALERTS)
    
    # бһ”бҹ’бһҡбһ¶бһ”бҹӢбһ–бҹҒбһӣбһҸбһҳбҹ’бһӣбҹғбһ”бҹҶбһ”бҹӮбһҖ PDL (Sellside Liquidity Taken)
    elif price < data['pdl']:
        alert = f"рҹҡЁ **SMC ALERT: PDL SWEEP!**\n\nбһҸбһҳбҹ’бһӣбҹғбһ”бһ¶бһ“бһ”бҹҶбһ”бҹӮбһҖ Low бһҳбҹ’бһҹбһ·бһӣбһҳбһ·бһү: `${price:,.2f}`\nвҡ пёҸ бһўбһ¶бһ…бһҮбһ¶бһҖбһ¶бһҡбһҷбһҖ Liquidity (SSL)бҹ” бһҡбһ„бҹӢбһ…бһ¶бҹҶбһҳбһҫбһӣбһҹбһүбҹ’бһүбһ¶ Reversal!"
        send_msg(alert, TOPIC_ALERTS)

if __name__ == "__main__":
    main()
        
